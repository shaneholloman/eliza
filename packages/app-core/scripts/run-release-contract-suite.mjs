/** Supports app-core build, packaging, or development orchestration for run release contract suite mjs. */
import { spawnSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";
import { resolveRepoRoot } from "./lib/repo-root.mjs";

const APP_CORE_ROOT = path.resolve(import.meta.dirname, "..");
const LOCAL_ELIZA_ROOT = path.resolve(APP_CORE_ROOT, "..", "..");
const REPO_ROOT = fs.existsSync(
  path.join(LOCAL_ELIZA_ROOT, "packages", "agent", "package.json"),
)
  ? LOCAL_ELIZA_ROOT
  : resolveRepoRoot(APP_CORE_ROOT);
const WRITE_BUILD_INFO_SCRIPT = fs.existsSync(
  path.join(REPO_ROOT, "packages", "scripts", "write-build-info.ts"),
)
  ? "packages/scripts/write-build-info.ts"
  : "scripts/write-build-info.ts";

const releaseContractTests = [
  "eliza/packages/app-core/scripts/asset-cdn.test.ts",
  "eliza/packages/app-core/scripts/docker-contract.test.ts",
  "eliza/packages/app-core/scripts/chrome-extension-release-surface.test.ts",
  "eliza/packages/app-core/scripts/electrobun-release-workflow-drift.test.ts",
  "eliza/packages/app-core/scripts/electrobun-test-workflow-drift.test.ts",
  "eliza/packages/app-core/scripts/release-check.test.ts",
  "eliza/packages/app-core/scripts/static-asset-manifest.test.ts",
];

function resolveExistingRepoPath(relativePath) {
  if (fs.existsSync(path.join(REPO_ROOT, relativePath))) {
    return relativePath;
  }

  if (relativePath.startsWith("eliza/")) {
    const flatPath = relativePath.slice("eliza/".length);
    if (fs.existsSync(path.join(REPO_ROOT, flatPath))) {
      return flatPath;
    }
  }

  return null;
}

function runExistingVitestTests(label, candidates) {
  const resolvedTests = [];
  const missingTests = [];

  for (const candidate of candidates) {
    const resolved = resolveExistingRepoPath(candidate);
    if (resolved) {
      resolvedTests.push(resolved);
    } else {
      missingTests.push(candidate);
    }
  }

  if (missingTests.length > 0) {
    console.warn(
      `[release-contract] ${label}: skipping ${missingTests.length} unavailable ported test(s): ${missingTests.join(", ")}`,
    );
  }

  if (resolvedTests.length === 0) {
    console.warn(`[release-contract] ${label}: no local tests to run.`);
    return;
  }

  run("bunx", ["vitest", "run", ...resolvedTests], REPO_ROOT);
}

function run(command, args, cwd = APP_CORE_ROOT) {
  const result = spawnSync(command, args, {
    cwd,
    stdio: "inherit",
    env: {
      ...process.env,
      NODE_NO_WARNINGS: process.env.NODE_NO_WARNINGS ?? "1",
    },
    shell: process.platform === "win32",
  });

  if (result.status !== 0) {
    process.exit(result.status ?? 1);
  }
}

function hasRootTsdownEntry() {
  return (
    fs.existsSync(path.join(REPO_ROOT, "tsdown.config.ts")) ||
    fs.existsSync(path.join(REPO_ROOT, "tsdown.config.mts")) ||
    fs.existsSync(path.join(REPO_ROOT, "tsdown.config.js")) ||
    fs.existsSync(path.join(REPO_ROOT, "src", "index.ts"))
  );
}

function ensureRootRuntimeBundle() {
  if (hasRootTsdownEntry()) {
    run("bunx", ["tsdown", "--fail-on-warn", "false"], REPO_ROOT);
    return;
  }
  const nestedEntry = path.join(
    REPO_ROOT,
    "eliza",
    "packages",
    "app-core",
    "src",
    "entry.ts",
  );
  const entryTarget = fs.existsSync(nestedEntry)
    ? "../eliza/packages/app-core/src/entry.ts"
    : "../packages/app-core/src/entry.ts";
  const distDir = path.join(REPO_ROOT, "dist");
  const entrySource = [
    "// auto-generated by run-release-contract-suite.mjs",
    "// Standalone elizaOS checkouts do not have a root tsdown entry.",
    `export * from ${JSON.stringify(entryTarget)};`,
    "",
  ].join("\n");

  fs.mkdirSync(distDir, { recursive: true });
  fs.writeFileSync(path.join(distDir, "entry.js"), entrySource);
  fs.writeFileSync(path.join(distDir, "index.js"), entrySource);
  fs.writeFileSync(
    path.join(distDir, "package.json"),
    `${JSON.stringify({ type: "module" })}\n`,
  );
}

runExistingVitestTests("release drift tests", releaseContractTests);
runExistingVitestTests("startup integration drift test", [
  "eliza/packages/app-core/scripts/startup-integration-script-drift.test.ts",
]);

// tsdown and the release check both resolve repo-root-relative entries/config.
ensureRootRuntimeBundle();
fs.mkdirSync(path.join(REPO_ROOT, "dist"), { recursive: true });
fs.writeFileSync(
  path.join(REPO_ROOT, "dist", "package.json"),
  '{"type":"module"}\n',
);
run("node", ["--import", "tsx", WRITE_BUILD_INFO_SCRIPT], REPO_ROOT);
run(
  "node",
  ["packages/app-core/scripts/write-homepage-release-data.mjs"],
  REPO_ROOT,
);
// Regenerate static asset manifest from the CI build output so hashes
// match what release:check will validate.
run("node", ["scripts/generate-static-asset-manifest.mjs"]);
run("bun", ["run", "release:check"], REPO_ROOT);
